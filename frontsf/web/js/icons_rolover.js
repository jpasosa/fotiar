$(document).ready(function(){
	$(".added").mouseover(function() {
			$(this).toggleClass("hover");
    })
	$(".added").mouseout(function() {
			$(this).toggleClass("hover");
    })
	$(".comprada").mouseover(function() {
		$(this).toggleClass("hover");
	})
	$(".comprada").mouseout(function() {
			$(this).toggleClass("hover");
	})
});